BinToPeppermint::
BinToPeppermint ( const char * name )
  : BinToColorMap ( name )
{
  m_reds.resize ( 256);
  m_greens.resize ( 256 );
  m_blues.resize ( 256 );

  for ( int i = 0; i < 256; i++ ) {
    m_reds  [i] = reds[i];
    m_greens[i] = greens[i];
    m_blues [i] = blues[i];
  }
}

BinToPeppermint::BinToPeppermint ( const BinToPeppermint & bin_to_color )
  : BinToColorMap ( bin_to_color )
{
}

BinToColor * BinToPeppermint::clone () const
{
  return new BinToPeppermint ( *this );
}

